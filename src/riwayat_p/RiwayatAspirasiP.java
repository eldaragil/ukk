    public void load_riwayat() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("No");
    model.addColumn("Tanggal");
    model.addColumn("Nama");
    model.addColumn("Isi Aspirasi");
    model.addColumn("Kategori");
    model.addColumn("Status");

    try {
        int no = 1;

        Connection conn = Koneksi.Koneksi.KoneksiDB();
        PreparedStatement pst;
        ResultSet res;

        // 🔥 LOGIKA ROLE DISINI
         {

            // PELAPOR → hanya sesuai NIK
            String sql = "SELECT * FROM aspirasi "
           + "WHERE nik=? "
           + "AND (status='terimakasih' OR status='diterapkan') "
           + "ORDER BY tanggal DESC";
            pst = conn.prepareStatement(sql);
            pst.setString(1, session.getNik());
        }

        res = pst.executeQuery();

        while (res.next()) {
            model.addRow(new Object[]{
                no++,
                res.getString("tanggal"),
                res.getString("nama"),
                res.getString("isi_aspirasi"),
                res.getString("kategori"),
                res.getString("status")
            });
        }

        tb_riwayat.setModel(model);
        

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal Load Data: " + e.getMessage());
    }

}

